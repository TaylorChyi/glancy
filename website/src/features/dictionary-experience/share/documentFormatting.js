/**
 * 背景：
 *  - 词典分享图片生成在多个阶段都需要对输入进行安全裁剪与格式归一化，过去零散函数分布于渲染逻辑中，难以维护与复用。
 * 目的：
 *  - 提供语义化的字符串格式化工具，确保所有构建/渲染步骤共享一致的裁剪策略，避免重复实现。
 * 关键决策与取舍：
 *  - 维持纯函数接口，便于单元测试与未来扩展更多格式策略；
 *  - 暂不引入第三方库，降低体积并保持完全可控的裁剪逻辑。
 * 影响范围：
 *  - 词典分享文档构建与画布渲染模块。
 * 演进与TODO：
 *  - 后续可在此补充多语言字符规整、富文本片段提取等能力。
 */

export const toTrimmedString = (value) => {
  if (value == null) return "";
  if (typeof value === "string") return value.trim();
  return String(value).trim();
};
